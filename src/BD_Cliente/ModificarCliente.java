    private void jButtonModificarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarVendedorActionPerformed

        //Cuando le damos en el boton Modificar
        //Creamos un objeto de la clase control cliente que tiene el metodo que necesitamos
        Control_Cliente change = new Control_Cliente();
        //Obtenemos los datos de las cajas de texto
        String code = jTextFieldCode.getText();
        String codigo = jTextFieldCodigo.getText();
        String nombre = jTextFieldNombre.getText();
        String apellido  = jTextFieldApellido.getText();
        String ced = jTextFieldCedula.getText();
        //Utilizamos el metodo modificar cliente y pasamos parametros
        change.ModificarCliente(code, codigo, nombre, apellido,ced);
        
    }//GEN-LAST:event_jButtonModificarVendedorActionPerformed

