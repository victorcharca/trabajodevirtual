    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        //Cuando damos clic en el boton buscar por codigo
        //Primero limpiamos la caja de texto
        jTextFieldCodigo.setText("");
        jTextFieldNombre.setText("");
        jTextFieldApellido.setText("");
        jTextFieldCedula.setText("");
        //Ejecutamos las consultas
        try {
            Connection conexion = ConexionConBaseDatos.getConexion();
            Statement comando = conexion.createStatement();
            ResultSet registro = comando.executeQuery("select idCliente,Nombre_Cliente,Apellido_Cliente,Cedula_Cliente from table_Cliente where idCliente=" + jTextFieldCode.getText());
            
            //Luego recorremos cada columna de la fila consultada
           if (registro.next() == true) {
               //Finalmente asignamos los datos a cad caja de texto correspondiente
                jTextFieldCodigo.setText(registro.getString("idCliente"));
                jTextFieldNombre.setText(registro.getString("Nombre_Cliente"));
                jTextFieldApellido.setText(registro.getString("Apellido_Cliente"));
                jTextFieldCedula.setText(registro.getString("Cedula_Cliente"));
            } 
           else {
               //Este mensaje es cuando la consulta no da ningun resultado
                JOptionPane.showMessageDialog(null,"No existe  codigo "+jTextFieldCode.getText());
            }
           //Cerramos la conexión
            conexion.close();
        } catch (SQLException ex) {
            //Mensaje en caso de error.
            JOptionPane.showMessageDialog(null,"Error "+ex);
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

